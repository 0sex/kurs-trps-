        # –î–ª—è –∫–∞–∂–¥–æ–≥–æ –±—Ä–µ–Ω–¥–∞ —Å–æ–∑–¥–∞–µ–º –≤–∞—Ä–∏–∞—Ü–∏–∏ (—Ä–∞–∑–ª–∏—á–Ω—ã–µ —Ñ–æ—Ä–º—ã –∏ –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª–∏)
        for brand_idx, brand_name in enumerate(brands):
            # –°–æ–∑–¥–∞–µ–º –Ω–µ—Å–∫–æ–ª—å–∫–æ –≤–∞—Ä–∏–∞—Ü–∏–π –æ–¥–Ω–æ–≥–æ –±—Ä–µ–Ω–¥–∞ (—Ä–∞–∑–Ω—ã–µ –¥–æ–∑–∏—Ä–æ–≤–∫–∏, —Ñ–æ—Ä–º—ã)
            for form_idx in range(1, 3):  # 2 –≤–∞—Ä–∏–∞—Ü–∏–∏ –Ω–∞ –±—Ä–µ–Ω–¥
                if drug_counter >= total_drugs_target:
                    break
                
                # –í—ã–±–∏—Ä–∞–µ–º —Ñ–æ—Ä–º—É –≤—ã–ø—É—Å–∫–∞
                form = random.choice(FORMS)
                
                # –í—ã–±–∏—Ä–∞–µ–º –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª—è
                manufacturer = random.choice(MANUFACTURERS)
                
                # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –±–∞–∑–æ–≤—É—é —Ü–µ–Ω—É —Å –≤–∞—Ä–∏–∞—Ü–∏–µ–π
                base_price = random.randint(30, 500)
                price_mult = random.choice(PRICE_MULT)
                price = int(base_price * price_mult)
                
                # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –¥–æ–∑–∏—Ä–æ–≤–∫—É
                dosages = ["50 –º–≥", "100 –º–≥", "250 –º–≥", "500 –º–≥", "1000 –º–≥", "5 –º–≥", "10 –º–≥", "20 –º–≥"]
                dosage = random.choice(dosages)
                
                # –°–æ–∑–¥–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –ø—Ä–µ–ø–∞—Ä–∞—Ç–∞
                drug_name = f"{brand_name} {dosage}" if form_idx == 1 else f"{brand_name} {dosage} ({form})"
                
                # –î–æ–±–∞–≤–ª—è–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã —Ñ–∞—Ä–º–∞–∫–æ–ª–æ–≥–∏—á–µ—Å–∫–∏—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤
                targets = base_pharm.get("targets", [])
                metabolism = base_pharm.get("metabolism", [])
                side_effects = base_pharm.get("side_effects", [])
                
